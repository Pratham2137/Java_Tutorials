package com.company;

public class CWH_04_Literals {

    public static void main(String[] args){
        // Integral literals
        byte age = 34;
        int age2 = 56;
        short age3 = 87;
        long ageDino = 566666666666666L;
        //character literal
        char ch = 'A';
        //decimal literal
        float f1 = 5.6f; // f denote the float value
        //By default, double is type for decimal
        double d1 = 4.66D;
        // boolean literal
        boolean a = true;
        System.out.println(age);
        System.out.println(age2);
        String str = "Pratham";
        System.out.println(str);
    }

}
